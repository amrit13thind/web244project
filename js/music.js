function playsong() {
  for (let i = 0; i < catalog.length; i++) {
    let div = document.createElement("div");
    div.classList.add("card", "text-center");
    div.setAttribute("onmouseenter", "heffect(this.id)");
    div.setAttribute("onmouseleave", "leffect(this.id)");
    div.style.width = "18rem";

    let img = document.createElement("img");
    img.classList.add("card-img-top");
    ////////// modification
    img.src = catalog[i].songpic;

    let audio = document.createElement("audio");

    audio.setAttribute("preload", "none");

    let h5 = document.createElement("h5");
    h5.classList.add("card-title", "text-center");
    ////////// modification
    h5.innerHTML = " " + catalog[i].song;

    let div2 = document.createElement("div");
    div2.classList.add("card-body");

    div.appendChild(img);
    div.appendChild(h5);
    div.appendChild(div2);

    div.appendChild(audio);

    ////////   assigning ID to the div
    div.setAttribute("id", `${i + 1}`);

    // div.setAttribute("class", `songcards`);

    /////// assigning attribute to buttonm

    //   terms.setAttribute(‘id’,‘para-1’);

    container2.append(div);
  }
}

